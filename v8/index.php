            <p><h3> Interested High Schools: </h3>
              High schools that wish to participate should have basic laboratory facilities such as blacktop benches, working sinks, gas lines and a fume hood. In the process of scheduling your project we will discuss basic equipment.  In most cases, we can provide necessary equipment.
              <br> <br>
              Teachers who participate in the project should be prepared to do some extra reading and some team teaching on the subjects we cover. Our aim is to have teachers comfortable enough with the project to be able to do it themselves, given the materials.
              <h3> Interested Undergraduates: </h3>
              Tufts students who are interested in participating should contact Project Administrator Karen O’Hagan. Students should be prepared to submit an unofficial transcript, a resume, and the name of a science professor who can be contacted as a reference.
              <br>
              Tufts students who participate in undergraduate research will be mentored by a graduate student and Postdocs as well as an undergraduate who has some experience in the laboratory. Faculty supervision will come in the form of lab meetings.
              Paid summer research fellowships are available; research is performed during the semester for credit.</p>
