          <p> <h3> Program Details </h3>
            The primary objective of the BioSeq project is to provide students with a genuine research experience. Specific grant activities include 
            <li> establishing a core sequencing facility for educational purposes at Tufts’ Medford campus,</li> 
            <li>creating an entry level research-based bioinformatics course for high school students,</li>
            <li> developing curricular units to describe bioinformatics concepts, </li> 
            <li>and continuing Professor Walt’s long time focus on outreach to local K-12 students and teachers in the area of science education.</li>
            <br>
            Location: Pearson-Michael Chemistry Complex, Medford and participating local schools in Somerville, Medford, Malden, and Chinatown.

            Anticipated outcome: The anticipated outcome of the project is that individuals who participate at all levels will have a greater understanding of and interest in bioinformatics and next generation sequencing.  Undergraduates will gain experience in the lab and high school students will get a taste of genuine contemporary research.</p>
